"use strict";
/*

There are three main primitives in JavaScript and TypeScript.

boolean - true or false values
number - whole numbers and floating point values
string - text values like "TypeScript Rocks"

Type Assignment
When creating a variable, there are two main ways TypeScript assigns a type:

Explicit
Implicit

*/
// Explicit
let Name = "balakumar";
console.log(Name);
let number_add = 10 + 20;
console.log(number_add);
let con_true_false = 10 < 20;
console.log(con_true_false);
// Implicit
let firstName = "hari kumar";
console.log(firstName);
let number_sub = 10 - 20;
console.log(number_sub);
let true_false = 10 >= 20;
console.log(true_false);
